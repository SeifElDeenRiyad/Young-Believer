package com.example.youngbeliever.models;

public class AsmaaAllahHosnaModel
{
    int asmAllah;
    int asmAllahMeaning;
    public AsmaaAllahHosnaModel(int asmAllah, int asmAllahMeaning)
    {
        this.asmAllah = asmAllah;
        this.asmAllahMeaning = asmAllahMeaning;
    }
    public int getAsmAllah()
    {
        return asmAllah;
    }
    public int getAsmAllahMeaning()
    {
        return asmAllahMeaning;
    }
}